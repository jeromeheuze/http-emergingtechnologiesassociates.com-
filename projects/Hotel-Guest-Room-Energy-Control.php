	<p>San Diego Gas & Electric (SDG&E) retained Daryl DeJean of Emerging Technologies Associates (ETA) as Project Manager to manage an innovative evaluation, measurement and verification (EM&V) of three hotel guest rooms HVAC direct digital control systems at four hotels in San Diego County. ETA worked side by side with California Lighting Technology Center (CLTC) in collecting the energy data and ensuring the integrity of the systems  during the test period. Daryl DeJean was the Project Manager for the assessment project working on site with SDG&E customers who were the host sites, four  manufacturers and the California Lighting Technology Center  and  the Western Colling Energy Efficiency Center engineering teams. Lee Stevens was project coordinator of two sites.</p>
    <p>This 21-month assessment is the first project to accurately measure the energy savings resulting from the installation of systems that control airflow in a hotel guest room and reduce wasted energy when rooms are unoccupied. The project is one of several that will be used by the State to evaluate the effectiveness of the technology in new versions of Title 24.</p>
    <p>Emerging Technologies Associates is managing a second project assessing a web-based  guest room control system for San Diego Gas & Electric. Working closely with the hotel team,  we are providing project management to determine the effectiveness of an integrated centralized guest room control system.</p>
