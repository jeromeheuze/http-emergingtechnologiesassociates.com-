	<p>Lee Stevens benchmarked Southwick Recreation Center (SRC)as part of the energy management strategy spearheaded by the Board of Directors. In 2009, SRC, a highly successful and growing sporting venue began a major expansion of its athletic facilities, adding two new fields.</p>
    <p>ETA was asked to help with the assessment of a new lighting system where the 7-year fluorescent lighting were degrading rapidly and required constant lamp replacement by a staff made up mainly of volunteers. The firm assisted in evaluating electronic HID as well as induction lighting in the hi bay gymnasium application and identified a high efficiency new electronic HID fixtures that allowed SRC to redeuce costly maintenance costs. It was also recommended that a dimming control system was installed to further reduce energy consumption. Prior to the start of the project, Lee Stevens benchmarked SCR's energy performance back to Fall of 2008 to create a baseline for the lighting project to ensure that persistent energy savings are realized by the non profit organization. The energy savings would help the SRC offset its operating costs and allow for additional activities and more children to partipate in the SRC's youth programs without impacting the budget.</p>
    
