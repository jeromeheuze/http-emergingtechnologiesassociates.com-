    <p>This report summarizes the functional performance and demand response (DR) potential of  three advanced lighting control technologies that were assessed in office environments in Northern California. The technologies assessed included the Adura LightPoint System, Universal Demand Control Lighting (DCL) System and DEMANDFlex ballast, and the Echoflex Solutions system.</p>
    <p>The assessment approach was two-fold, including functional testing of the system (i.e. did the control technology work as intended) and quantification of load shedding resulting from use of the controls.  The control strategies were also evaluated for ease of installation, reliability, and customer acceptance.  Additional technology performance criteria, including system costs, are presented in the report.</p>
