    <h2 class="th2">American Recovery and Reinvestment Act of 2009: Energy Training Partnership Grants</h2>
    <h3 class="th3">Recovery Act: Competitive Grants for Green Job Training</h3>
    <p>On February 17, 2009, President Barack Obama signed into law the American Recovery and Reinvestment Act of 2009 (Recovery Act) to preserve and create jobs, promote the nation's economic recovery, and assist those most impacted by the recession. The purpose of these grants is to teach workers the skills required in emerging industries including energy efficiency and renewable energy.</p>
    <h3 class="th3">Energy Training Partnership Grants Overview</h3>
    <p>The Energy Training Partnership grant program will provide training for workers that prepares them to enter the energy efficiency and renewable energy industries, as well as green occupations within other industries. These grants invest in partnerships made up of diverse set of stakeholders including labor organizations, public or private employers in the energy efficiency and renewable energy industries, and the workforce system. Bringing together the workforce expertise of these groups will allow grantees to develop programs that are responsive to the needs of both workers and employers, and that provide participants with the support needed to successfully complete training. The grantees will utilize these partnerships to design and distribute training approaches that lead to portable industry credentials and employment, including registered apprenticeship. The Department of Labor awarded grants to workforce development projects that focused on connecting target populations to career pathways in green industries. The Department also was interested in projects that contributed to our understanding of green industries and jobs that clean and enhance our environment.</p>
    <p>These investments are designed to achieve the following goals:</p>
    <ul class="ullist">
    <li>Provide training and placement services in the energy efficiency and renewable energy industries for workers impacted by national energy and environmental policy, individuals in need of updated training related to the energy efficiency and renewable energy industries, and unemployed workers.</li>
    <li>All training and placement activities funded through this grant program will be conducted at the local level.</li>
    <li>A portion of the total funds—approximately $25 million—are reserved for projects that will serve communities impacted by automotive industryrelated restructuring.</li>
    </ul>
