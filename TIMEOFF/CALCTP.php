    <h1 class="th1">California Advanced Lighting Controls Training Program (CALCTP)</h1>
	<div class="row">
	<p>In June 2008. Emerging Technologies Associates (ETA) was retained by Southern California Edison, Pacific Gas & Electric, San Diego Gas & Electric, the National Electrical Contractors Association (NECA), the International Brotherhood of Electrical Workers (IBEW) and the California Community College System to create part of the Business Section for a new statewide training program on Advanced Lighting Control Systems.</p>
    <p>The training initiative was named California Advanced Lighting Controls Program (CALCTP). This course is part of a statewide workforce development initiative to create a certfication process that will ensure persistent energy savings as a result of the proper installation and commissioning of advanced lighting control systems.</p>
    <p>For more details on the goals of the CALCTP for the State of California, you can click on the following link:</p>
    <p><a href="https://www.calctp.org/" target="_blank">CALCTP website</a></p>
    <p>Emerging Technologies Associates (ETA) and Kingport Corporation created the Market Drivers and Financing Projects modules for the course and conducted the initial four Train the Trainer sessions for selected participants of the program. In addition, Emerging Technologies Associates screened and selected financial institutions and other financial service providers for the initiative to finance energy efficiency projects resulting from the CALCTP Initiative.</p>
    <p>The CALCTP Training Program is being taught throughout the state of California at many IBEW-NECA Training Centers. Please refer to our Seminar Pages for Upcoming Training Sessions.</p>
    <p>In  July and August 2010, ETA taught 30 NECA and IBEW members during two seminars in Northern California under the sponsorship of IBEW-NECA Central Coast and Northern California Chapters. Several contractors immediately put the seminar material to use.</p>
    <p>Emerging Technologies Associates was retained in the Fall of 2011 to conduct a California statewide marketing and education outreach for the CALCTP Program. The outreach project is to be completed on July 1, 2012.</p>
    <p>We have just completed another successful all day CALCTP seminar. This is the first CALCTP Business seminar conducted in Orange County.</p>

