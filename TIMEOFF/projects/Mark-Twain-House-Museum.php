	<p>The Mark Twain House Museum was looking for ways to reduce its operting costs.</p>
    <p>Working with the Museum, Emerging Technologies Associates (ETA) reviewed the existing 400 plus incandescent and fluorescent lamps and fixtures. ETA designed a lighting configuration that resulted in the retrofit of the existing lighting fixtures to be switched to LED lamps throughout the museum exhibit area, retail space, storage areas and office space.</p>
    <p>By implementing this lighting retrofit and undertaking other energy efficiency measures, the Museum was able to raise additional funds from private donors. The additional savings provided the Museum the opportunity to undertake a heating and ventilation air conditioning (HVAC) analysis and potential retrofit.</p>
    <p>The Museum was featured in a CBS national broadcast and several articles published by The Wall Street Journal and New York Times. In February 2009, the Museum Director reported a reduction in the monthly electric bills from over $10,000 to $5,000 resulting from the lighting retrofit and other energy efficiency measures implemented by the Museum.</p>
    
