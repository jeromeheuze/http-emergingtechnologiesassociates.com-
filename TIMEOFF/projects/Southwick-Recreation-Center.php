	<p>Even though the Recreation Center is not a ratable building at this time, the volunteers worked for two days to plan, design and install new electronic High Intensity Discharge lights, replacing fluorescent lights that were constantly failing.  Wanting to track their building energy consumption prior to the lighting retrofit, they solicited Emerging Technologies Associates help in entering their utility bills into the ENERGY STAR Portfolio Manager system. Southwick now has a baseline of energy use prior to changing out the lights. They can use it to track the savings from the lighting retrofit. Furthermore, with the expansion of the activities at the Center, it is anticipated that further energy use would be forthcoming. Benchmarking in ENERGY STAR will allow Southwick to have a true picture of its energy consumption and continue to conserve.</p>
    
