	<p>Halogen MR16 lamps were replaced with LED DLR lamps in select fixtures throughout the store. The LED DLR lamp technology was tested as a replacement for 50W halogen MR16 lamps that were in use within the store. The suitability of the new technology was determined by energy and power usage measurements, lighting performance characteristics, qualitative satisfaction, and economic factors.</p>
    <p>For every 50 watt halogen MR16 lamp removed, one LED DLR was installed. The LED DLR lamps tested, in a newly installed recessed can fixture, resulted in an average draw of 15.2 watts, roughly 75% (44.9 watts) less than when the original fixture powered a 50W halogen lamp.</p>
    <p>This project was crucial in demonstrating the need for integrating LED lighting and halogen lighting technologies in order to achieve the proper light levels in various spaces and applications within a retail store.</p>
    
